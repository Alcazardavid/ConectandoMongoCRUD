const mongoose = require('mongoose');

//Definicion del esquema del usuario
const UserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});
//creacion y exportacion del modelo user
module.exports = mongoose.model('User', UserSchema);